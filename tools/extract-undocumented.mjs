import { readdirSync, readFileSync, statSync, existsSync } from 'fs';
import { join } from 'path';

const ver = process.argv[2] || '1.3.15';
const root = `bannerlord-${ver}`;
const docsRoot = `BannerlordCode.github.io/docs/v${ver}`;
const NOISE = /^(SandBox\.GauntletUI\.AutoGenerated|.*\.AutoGenerated|Steamworks|Galaxy|Newtonsoft|NetworkMessages\.|Messages\.|ManagedCallbacks|.*\.CodeGenerator|.*GeneratedMessages|JetBrains\.|Harmony|Mono\.|Microsoft\.|Alex\.|nunit|Xceed|LiteDB|MessagePack|ZeroMQ|Razor|Selenium|Cef|WebDriver|Svg|Ookii|Custom\.|NLog)/;

function walk(dir, acc = []) {
  let entries;
  try { entries = readdirSync(dir); } catch { return acc; }
  for (const entry of entries) {
    if (entry.startsWith('.') || entry === 'node_modules') continue;
    const fullPath = join(dir, entry);
    let stats;
    try { stats = statSync(fullPath); } catch { continue; }
    if (stats.isDirectory()) {
      walk(fullPath, acc);
    } else if (entry.endsWith('.cs')) {
      acc.push(fullPath);
    }
  }
  return acc;
}

function sourceKey(namespace, name) {
  return `${namespace}|${name}`;
}

function collectSourceTypes() {
  const result = [];
  const files = walk(root);
  const typeRegex = /^\s*public\s+(?:sealed\s+|abstract\s+|static\s+|partial\s+|new\s+)*(class|struct|interface|enum)\s+(\w+)/gm;
  for (const filePath of files) {
    const raw = readFileSync(filePath, 'utf8');
    const namespace = raw.match(/namespace\s+([\w.]+)/)?.[1] || '(global)';
    if (NOISE.test(namespace)) continue;
    let match;
    typeRegex.lastIndex = 0;
    while ((match = typeRegex.exec(raw))) {
      result.push({ namespace, name: match[2] });
    }
  }
  return result;
}

function collectDocTypes() {
  const result = new Set();
  for (const lang of ['en', 'zh']) {
    const apiRoot = join(docsRoot, lang, 'api');
    if (!existsSync(apiRoot)) continue;
    const entries = [];
    (function walkDocs(dir) {
      let items;
      try { items = readdirSync(dir); } catch { return; }
      for (const item of items) {
        const fullPath = join(dir, item);
        let stats;
        try { stats = statSync(fullPath); } catch { continue; }
        if (stats.isDirectory()) {
          walkDocs(fullPath);
        } else if (item.endsWith('.md') && item !== 'index.md') {
          entries.push(fullPath);
        }
      }
    })(apiRoot);

    for (const filePath of entries) {
      const text = readFileSync(filePath, 'utf8');
      const title = text.match(/^#\s+(.+)$/m)?.[1]?.trim().replace(/\s*\(v\d+\.\d+\.\d+\)$/, '') || filePath.replace(/\\/g, '/').split('/').pop()?.replace(/\.md$/, '').split('__')[0] || '';
      const namespaceLine = text.split(/\r?\n/).find((line) => line.startsWith('**Namespace:**') || line.startsWith('**命名空间:**')) || '';
      const namespace = namespaceLine.replace(/^\*\*(?:Namespace|命名空间):\*\*\s*/, '').trim();
      if (title && namespace) {
        result.add(sourceKey(namespace, title));
      }
    }
  }
  return result;
}

const sourceTypes = collectSourceTypes();
const docTypes = collectDocTypes();
const missing = sourceTypes
  .filter((item) => {
    const exactKey = sourceKey(item.namespace, item.name);
    return !docTypes.has(exactKey);
  })
  .map((item) => sourceKey(item.namespace, item.name))
  .sort();

for (const key of missing) {
  console.log(key);
}

console.error(`Total undocumented: ${missing.length}`);
