// generate-inventory.mjs
//
// Builds a type inventory manifest across Bannerlord source versions
// (1.3.0, 1.3.15, 1.4.5) and checks each type against the existing
// bilingual (zh/en) doc pages under content/v<ver>/<lang>/api/.
//
// Output:
//   tools/data/type-inventory.json   stable-keyed manifest
//   stdout                           per-version, per-language counts
//   .omo/notes/type-inventory-summary.md   saved counts report
//
// Run:  node tools/generate-inventory.mjs   (from repo root)

import { readdirSync, readFileSync, statSync, mkdirSync, writeFileSync, existsSync } from 'node:fs';
import { join, relative, sep, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');
const CONTENT_ROOT = join(REPO_ROOT, 'content');

// ---------------------------------------------------------------------------
// Source tree configuration
// ---------------------------------------------------------------------------
// Each version lists one or more roots (relative to repo root). For 1.4.5 the
// whole Bannerlord.Source tree is walked once; files under bin/ are flagged so
// dedup can prefer the decompiled bin/ copy over the module source copy.
const VERSIONS = [
  {
    version: '1.3.0',
    roots: [{ dir: '../bannerlord-1.3.0', binRoot: false }],
  },
  {
    version: '1.3.15',
    roots: [{ dir: '../bannerlord-1.3.15', binRoot: false }],
  },
  {
    version: '1.4.5',
    roots: [{ dir: '../bannerlord-1.4.5/Bannerlord.Source', binRoot: false }],
  },
];

// ---------------------------------------------------------------------------
// Noise namespace filter
// ---------------------------------------------------------------------------
// Third-party roots + Bannerlord-generated glue that the doc scope excludes.
// Case-insensitive: decompiled source uses varying capitalization (Jose / jose).
const NOISE_PREFIX = /^(Steamworks|Galaxy|GalaxyCSharp|Newtonsoft|System|jose|JetBrains|Harmony|Mono|Microsoft|Alex|nunit|Xceed|LiteDB|MessagePack|ZeroMQ|Razor|Selenium|Cef|WebDriver|Svg|Ookii|NLog|Custom|ManagedCallbacks|FxResources|Security)(\.|$)/i;

// Third-party assembly folders under bin/ whose types are entirely out of scope.
const THIRD_PARTY_ASSEMBLIES = new Set([
  'GalaxyCSharp',
  'jose-jwt',
  'Newtonsoft.Json',
  'Steamworks.NET',
  'System.Management',
  'System.Numerics.Vectors',
]);

function isNoiseNamespace(ns) {
  if (!ns) return false;
  if (NOISE_PREFIX.test(ns)) return true;
  const segs = ns.split('.');
  for (const s of segs) {
    if (s === 'CodeGenerator' || s === 'GeneratedMessages') return true;
    if (/^AutoGenerated\d*$/.test(s)) return true;
  }
  return false;
}

