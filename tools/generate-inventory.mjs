// ---------------------------------------------------------------------------
// Filesystem walker
// ---------------------------------------------------------------------------
function walkCsFiles(dir) {
  const out = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return out;
  }
  for (const e of entries) {
    if (e.name.startsWith('.') || e.name === 'node_modules') continue;
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      walkCs(full, out);
    } else if (e.isFile() && e.name.endsWith('.cs')) {
      out.push(full);
    }
  }
  return out;
}
function walkCs(dir, acc) {
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const e of entries) {
    if (e.name.startsWith('.') || e.name === 'node_modules') continue;
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      walkCs(full, acc);
    } else if (e.isFile() && e.name.endsWith('.cs')) {
      acc.push(full);
    }
  }
}

function walkMdFiles(dir) {
  const out = [];
  if (!existsSync(dir)) return out;
  (function rec(d) {
    let entries;
    try {
      entries = readdirSync(d, { withFileTypes: true });
    } catch {
      return;
    }
    for (const e of entries) {
      if (e.name.startsWith('.')) continue;
      const full = join(d, e.name);
      if (e.isDirectory()) rec(full);
      else if (e.isFile() && e.name.endsWith('.md')) out.push(full);
    }
  })(dir);
  return out;
}

// ---------------------------------------------------------------------------
// C# type parsing
// ---------------------------------------------------------------------------
// Matches a contiguous run of modifier keywords immediately followed by a type
// keyword and the type identifier. Allows an optional leading [Attribute] on
// the same line (rare in decompiled source but safe).
const TYPE_RE =
  /^\s*(?:\[[^\]]*\]\s*)*((?:(?:public|protected|internal|private|static|abstract|sealed|partial|readonly|extern|async|unsafe|new|virtual|override|volatile)\s+)+)(class|struct|interface|enum)\s+([A-Za-z_]\w*)/;

const NS_RE = /^\s*namespace\s+([A-Za-z_][\w.]*)\s*[;{]/m;

const ACCESSIBILITY = new Set(['public', 'protected', 'internal']);

function parseFile(content) {
  // Strip BOM.
  if (content.charCodeAt(0) === 0xfeff) content = content.slice(1);
  const nsMatch = content.match(NS_RE);
  const declaredNs = nsMatch ? nsMatch[1] : null;
  const types = [];
  const lines = content.split(/\r?\n/);
  for (const line of lines) {
    const m = line.match(TYPE_RE);
    if (!m) continue;
    const mods = m[1].trim().split(/\s+/);
    if (mods.includes('private')) continue; // skip private / private protected
    let hasAccess = false;
    for (const mod of mods) if (ACCESSIBILITY.has(mod)) { hasAccess = true; break; }
    if (!hasAccess) continue; // require explicit public/protected/internal
    types.push({ kind: m[2], typeName: m[3] });
  }
  return { declaredNs, types };
}

// Determine the fallback namespace from the path: the assembly folder. For a
// bin/ tree the assembly is the segment right after `bin`; otherwise it is the
// first segment under the source root.
function fallbackNamespace(filePath, rootAbs, binRoot) {
  const rel = relative(rootAbs, filePath).split(/[\\/]/);
  if (binRoot) {
    // rootAbs already points at bin/; assembly = first segment.
    return rel[0] || null;
  }
  // For 1.4.5 Bannerlord.Source the first segment may be `bin` or `Modules.*`.
  if (rel[0] === 'bin') return rel[1] || rel[0] || null;
  return rel[0] || null;
}

// Assembly folder name for a source file (used to skip entire third-party DLLs).
function assemblyOf(filePath, rootAbs, binRoot) {
  const rel = relative(rootAbs, filePath).split(/[\\/]/);
  if (binRoot) return rel[0] || null;
  if (rel[0] === 'bin') return rel[1] || null;
  return rel[0] || null;
}

// ---------------------------------------------------------------------------
// Collect source types for one version (dedup preferring bin/)
// ---------------------------------------------------------------------------
function collectVersionTypes(cfg) {
  const byKey = new Map(); // `${namespace}\0${typeName}` -> {kind, sourceFile, isBin}
  let scanned = 0;
  for (const root of cfg.roots) {
    const rootAbs = join(REPO_ROOT, root.dir);
    if (!existsSync(rootAbs)) {
      process.stderr.write(`  WARN: source root missing: ${root.dir}\n`);
      continue;
    }
    // binRoot=true means rootAbs IS the bin folder; otherwise detect bin under it.
    const binRoot = root.binRoot === true;
    const files = walkCsFiles(rootAbs);
    process.stderr.write(`  ${root.dir}: ${files.length} .cs files\n`);
    for (const filePath of files) {
      scanned++;
      // Skip entire third-party DLLs (Galaxy, jose, Newtonsoft, Steamworks,
      // System.Management, System.Numerics.Vectors) by assembly folder.
      const assembly = assemblyOf(filePath, rootAbs, binRoot);
      if (assembly && THIRD_PARTY_ASSEMBLIES.has(assembly)) continue;
      let content;
      try {
        content = readFileSync(filePath, 'utf8');
      } catch {
        continue;
      }
      const { declaredNs, types } = parseFile(content);
      let namespace = declaredNs;
      if (!namespace) {
        namespace = fallbackNamespace(filePath, rootAbs, binRoot);
      }
      if (!namespace) continue;
      if (isNoiseNamespace(namespace)) continue;
      const isBin =
        binRoot ||
        filePath.split(sep).includes('bin') ||
        filePath.includes('/bin/') ||
        filePath.includes('\\bin\\');
      const sourceFile = relative(REPO_ROOT, filePath).split(/[\\/]/).join('/');
      for (const t of types) {
        const key = `${namespace}\0${t.typeName}`;
        const existing = byKey.get(key);
        if (existing) {
          // Prefer bin/ copy on conflict.
          if (isBin && !existing.isBin) {
            byKey.set(key, { kind: t.kind, sourceFile, isBin });
          }
          continue;
        }
        byKey.set(key, { kind: t.kind, sourceFile, isBin });
      }
    }
  }
  process.stderr.write(`  scanned ${scanned} files, ${byKey.size} unique types\n`);
  return byKey;
}

// ---------------------------------------------------------------------------
// Doc index per (version, language)
// ---------------------------------------------------------------------------
// plainMap:  fullTypeName -> [{ area, path }]
// suffixMap: typeName (before first __) -> [{ suffix, area, path }]
function buildDocIndex(ver, lang) {
  const apiRoot = join(CONTENT_ROOT, `v${ver}`, lang, 'api');
  const files = walkMdFiles(apiRoot);
  const plainMap = new Map();
  const suffixMap = new Map();
  const areas = new Set();
  for (const f of files) {
    const base = f.slice(f.lastIndexOf(sep) + 1).replace(/\.md$/i, '');
    if (base === '_index') continue;
    const area = relative(apiRoot, dirname(f)).split(/[\\/]/).join('/');
    areas.add(area);
    const path = relative(REPO_ROOT, f).split(/[\\/]/).join('/');
    const entry = { area, path };
    // plain (literal name) -- always indexed under the full base name
    if (!plainMap.has(base)) plainMap.set(base, []);
    plainMap.get(base).push(entry);
    // collision-suffix interpretation (only when a __ is present)
    const dunder = base.indexOf('__');
    if (dunder > 0) {
      const typeName = base.slice(0, dunder);
      const suffix = base.slice(dunder + 2);
      if (!suffixMap.has(typeName)) suffixMap.set(typeName, []);
      suffixMap.get(typeName).push({ suffix, area, path });
    }
  }
  // sort each list by area then path for stable selection
  const sortFn = (a, b) => (a.area < b.area ? -1 : a.area > b.area ? 1 : a.path < b.path ? -1 : a.path > b.path ? 1 : 0);
  for (const arr of plainMap.values()) arr.sort(sortFn);
  for (const arr of suffixMap.values()) arr.sort(sortFn);
  return { plainMap, suffixMap, areas };
}

// ---------------------------------------------------------------------------
// Match a source type against a doc index
// ---------------------------------------------------------------------------
function matchDoc(namespace, typeName, predictedArea, index, version, lang) {
  const nsSuffix = namespace.replace(/\./g, '_');
  // 1. namespace-aware collision-suffix match (exact)
  const suffixed = index.suffixMap.get(typeName);
  if (suffixed) {
    const hit = suffixed.find((e) => e.suffix === nsSuffix);
    if (hit) return { hasDoc: true, pagePath: hit.path };
  }
  // 2. plain filename fallback (unique types + literal __ names)
  const plain = index.plainMap.get(typeName);
  if (plain && plain.length) {
    // prefer the predicted area if present, else the first (stable)
    const inArea = plain.find((e) => e.area === predictedArea);
    const chosen = inArea || plain[0];
    return { hasDoc: true, pagePath: chosen.path };
  }
  // 3. not documented -> predicted expected path for downstream stub generation
  return {
    hasDoc: false,
    pagePath: `content/v${version}/${lang}/api/${predictedArea}/${typeName}.md`,
  };
}

// ---------------------------------------------------------------------------
// Stable nested object builder
// ---------------------------------------------------------------------------
function sortedObject(map, valueFn) {
  const keys = [...map.keys()].sort();
  const obj = {};
  for (const k of keys) obj[k] = valueFn(k);
  return obj;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
function main() {
  const manifest = {};
  const summary = [];

  for (const cfg of VERSIONS) {
    process.stderr.write(`\n=== v${cfg.version}: collecting source types ===\n`);
    const typeMap = collectVersionTypes(cfg);
    const languages = ['en', 'zh'].filter((l) =>
      existsSync(join(CONTENT_ROOT, `v${cfg.version}`, l, 'api'))
    );
    if (languages.length === 0) {
      process.stderr.write(`  WARN: no api docs for v${cfg.version}\n`);
    }
    manifest[cfg.version] = {};
    for (const lang of languages.sort()) {
      process.stderr.write(`  building doc index for ${lang}...`);
      const index = buildDocIndex(cfg.version, lang);
      process.stderr.write(` ${index.plainMap.size} plain, ${index.suffixMap.size} suffix\n`);

      // Group types by namespace, sorted.
      const byNs = new Map();
      for (const [key, info] of typeMap) {
        const [ns, typeName] = key.split('\0');
        if (!byNs.has(ns)) byNs.set(ns, new Map());
        byNs.get(ns).set(typeName, info);
      }

      let total = 0;
      let documented = 0;
      const langObj = sortedObject(byNs, (ns) => {
        const typeMapNs = byNs.get(ns);
        const tkeys = [...typeMapNs.keys()].sort();
        const nsObj = {};
        for (const typeName of tkeys) {
          const info = typeMapNs.get(typeName);
          const predictedArea = predictArea(ns);
          const { hasDoc, pagePath } = matchDoc(ns, typeName, predictedArea, index, cfg.version, lang);
          total++;
          if (hasDoc) documented++;
          nsObj[typeName] = {
            kind: info.kind,
            hasDoc,
            pagePath,
            sourceFile: info.sourceFile,
          };
        }
        return nsObj;
      });

      manifest[cfg.version][lang] = langObj;
      const missing = total - documented;
      summary.push({ version: cfg.version, lang, total, documented, missing });
      process.stderr.write(
        `  ${lang}: total=${total} documented=${documented} missing=${missing}\n`
      );
    }
  }

  // Write JSON.
  const dataDir = join(__dirname, 'data');
  if (!existsSync(dataDir)) mkdirSync(dataDir, { recursive: true });
  const outPath = join(dataDir, 'type-inventory.json');
  writeFileSync(outPath, JSON.stringify(manifest, null, 2), 'utf8');
  process.stderr.write(`\nWrote ${outPath}\n`);

  // Print summary to stdout.
  const lines = [];
  lines.push('Type Inventory Summary');
  lines.push('=====================');
  lines.push('');
  lines.push('| Version | Language | Total | Documented | Missing |');
  lines.push('|---------|----------|-------|------------|---------|');
  for (const s of summary) {
    lines.push(
      `| ${s.version} | ${s.lang} | ${s.total} | ${s.documented} | ${s.missing} |`
    );
  }
  lines.push('');
  for (const s of summary) {
    lines.push(`v${s.version}/${s.lang}: total=${s.total} documented=${s.documented} missing=${s.missing}`);
  }
  const stdoutText = lines.join('\n');
  console.log(stdoutText);

  // Save summary report.
  const notesDir = join(REPO_ROOT, '.omo', 'notes');
  if (!existsSync(notesDir)) mkdirSync(notesDir, { recursive: true });
  const report = [];
  report.push('# Type Inventory Summary');
  report.push('');
  report.push('Generated by `tools/generate-inventory.mjs`.');
  report.push('');
  report.push('## Counts');
  report.push('');
  report.push('| Version | Language | Total | Documented | Missing |');
  report.push('|---------|----------|-------|------------|---------|');
  for (const s of summary) {
    report.push(`| ${s.version} | ${s.lang} | ${s.total} | ${s.documented} | ${s.missing} |`);
  }
  report.push('');
  report.push('## Per-version / per-language');
  report.push('');
  for (const s of summary) {
    report.push(`- **v${s.version}/${s.lang}** — total: ${s.total}, documented: ${s.documented}, missing: ${s.missing}`);
  }
  report.push('');
  report.push('## Methodology');
  report.push('');
  report.push('- Parses `class`, `struct`, `interface`, `enum` declarations with explicit `public`, `protected`, or `internal` modifiers (skips `private` and `private protected`).');
  report.push('- Source roots: 1.3.0 `../bannerlord-1.3.0`, 1.3.15 `../bannerlord-1.3.15`, 1.4.5 `../bannerlord-1.4.5/Bannerlord.Source` (bin/ copies preferred on namespace+typeName dedup).');
  report.push('- Skips third-party/noise namespaces (Steamworks, Galaxy, Newtonsoft, System.*, jose, JetBrains, Harmony, Mono, Microsoft, AutoGenerated*, CodeGenerator, GeneratedMessages, ManagedCallbacks, etc.) while keeping game-level networking/message namespaces.');
  report.push('- `hasDoc`: namespace-aware collision-suffix match (`TypeName__Namespace_With_Underscores.md`) first, then plain filename fallback (`TypeName.md`, also covers literal `__` widget names).');
  report.push('- `pagePath`: actual content-relative path when documented; predicted expected path (`content/v<ver>/<lang>/api/<area>/TypeName.md`) when missing.');
  report.push('- Manifest: `tools/data/type-inventory.json` (stable key ordering).');
  writeFileSync(join(notesDir, 'type-inventory-summary.md'), report.join('\n') + '\n', 'utf8');
  process.stderr.write('Wrote .omo/notes/type-inventory-summary.md\n');
}

main();
