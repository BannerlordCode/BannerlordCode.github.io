// tools/gen-class-catalog.mjs
// Generate a complete public-type catalog from 1.3.15 source, grouped by namespace area.
// Lists every public class/struct/interface/enum so modders have the full map;
// types that have a deep doc page are linked. Run from repo root.
import { readdirSync, readFileSync, statSync, existsSync, writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const VER = process.argv[2] || '1.3.15';
const ROOT = 'bannerlord-' + VER;
const OUT = 'BannerlordCode.github.io/docs/v' + VER;
const NOISE = /^(SandBox\.GauntletUI\.AutoGenerated|.*\.AutoGenerated|Steamworks|Galaxy|Newtonsoft|NetworkMessages\.|Messages\.|ManagedCallbacks|.*\.CodeGenerator|.*GeneratedMessages|JetBrains\.|Harmony|Mono\.|Microsoft\.|Alex\.|nunit|Xceed|LiteDB|MessagePack|ZeroMQ|Razor|Selenium|Cef|WebDriver|Svg|Ookii|Custom\.|NLog)/;

function walk(d, a = []) {
  let e; try { e = readdirSync(d); } catch { return a; }
  for (const x of e) {
    if (x.startsWith('.') || x === 'node_modules') continue;
    const p = join(d, x);
    let s; try { s = statSync(p); } catch { continue; }
    if (s.isDirectory()) walk(p, a);
    else if (x.endsWith('.cs')) a.push(p);
  }
  return a;
}

const reType = /^\s*public\s+(?:sealed\s+|abstract\s+|static\s+|partial\s+|new\s+)*(class|struct|interface|enum)\s+(\w+)/gm;

// Collect: ns -> [{name, kind, file}]
const byNs = {};
const files = walk(ROOT);
for (const f of files) {
  let t; try { t = readFileSync(f, 'utf8'); } catch { continue; }
  const nsM = t.match(/namespace\s+([\w.]+)/);
  const ns = nsM ? nsM[1] : '(global)';
  if (NOISE.test(ns)) continue;
  let m; reType.lastIndex = 0;
  while ((m = reType.exec(t))) {
    (byNs[ns] = byNs[ns] || []).push({ name: m[2], kind: m[1], file: f.replace(/\\/g, '/').replace(ROOT + '/', '') });
  }
}
// dedupe by (ns,name) — partial classes appear multiple times
for (const ns of Object.keys(byNs)) {
  const seen = new Set(); byNs[ns] = byNs[ns].filter(x => { const k = x.name; if (seen.has(k)) return false; seen.add(k); return true; });
}

// Discover documented class page basenames.
const documented = new Set();
const relCurrent = {};
const rel315 = {};
const versionPage = new Set();
function docKey(ns, name) { return `${ns}|${name}`; }
function parseDocMeta(path) {
  const text = readFileSync(path, 'utf8');
  const title = text.match(/^#\s+(.+)$/m)?.[1]?.trim().replace(/\s*\(v\d+\.\d+\.\d+\)$/, '') || path.replace(/\\/g, '/').split('/').pop()?.replace(/\.md$/, '').split('__')[0] || '';
  const namespaceLine = text.split(/\r?\n/).find((line) => line.startsWith('**Namespace:**') || line.startsWith('**命名空间:**')) || '';
  const namespace = namespaceLine.replace(/^\*\*(?:Namespace|命名空间):\*\*\s*/, '').trim();
  if (!title || !namespace) return null;
  return { key: docKey(namespace, title), rel: path.replace(/\\/g, '/').replace(/.*\/api\//, '') };
}
function docWalk(d, set, relMap) {
  let e; try { e = readdirSync(d); } catch { return; }
  for (const x of e) {
    const p = join(d, x);
    let s; try { s = statSync(p); } catch { continue; }
    if (s.isDirectory()) {
      docWalk(p, set, relMap);
    } else if (x.endsWith('.md') && x !== 'index.md') {
      const meta = parseDocMeta(p);
      if (!meta) continue;
      set.add(meta.key);
      if (relMap) {
        const current = relMap[meta.key];
        if (!current) {
          relMap[meta.key] = meta.rel;
        } else if (current.includes('-ext/') && !meta.rel.includes('-ext/')) {
          relMap[meta.key] = meta.rel;
        }
      }
    }
  }
}
docWalk(join(OUT, 'en/api'), documented, relCurrent);
if (VER !== '1.3.15') {
  docWalk(join('BannerlordCode.github.io/docs/v1.3.15/en/api'), documented, rel315);
  docWalk(join('BannerlordCode.github.io/docs/versions'), versionPage, null);
}

function docLink(ns, name, lang) {
  const key = docKey(ns, name);
  if (relCurrent[key]) return './' + relCurrent[key];
  if (VER === '1.3.15') return null;
  if (rel315[key]) return `/v1.3.15/${lang}/api/${rel315[key]}`;
  if (versionPage.has(name)) return `/versions/${name}`;
  return null;
}
function isDocumented(ns, name, lang) {
  return docLink(ns, name, lang) !== null;
}

// Area mapping: top-level grouping
function areaOf(ns) {
  if (ns.startsWith('TaleWorlds.CampaignSystem')) return 'campaign';
  if (ns.startsWith('TaleWorlds.MountAndBlade')) return 'mountandblade';
  if (ns === 'TaleWorlds.Core' || ns.startsWith('TaleWorlds.Core.')) return 'core';
  if (ns.startsWith('TaleWorlds.Engine')) return 'engine';
  if (ns.startsWith('TaleWorlds.Library')) return 'library';
  if (ns.startsWith('TaleWorlds.GauntletUI') || ns.startsWith('TaleWorlds.TwoDimension')) return 'ui';
  if (ns.startsWith('TaleWorlds.SaveSystem')) return 'save';
  if (ns.startsWith('TaleWorlds.Network') || ns.startsWith('TaleWorlds.DotNet') || ns.startsWith('TaleWorlds.Diamond') || ns.startsWith('TaleWorlds.PlatformService') || ns.startsWith('TaleWorlds.PlayerServices')) return 'system';
  if (ns.startsWith('SandBox') || ns.startsWith('StoryMode')) return 'gameplay';
  if (ns.startsWith('TaleWorlds.Localization')) return 'localization';
  if (ns.startsWith('TaleWorlds.InputSystem')) return 'input';
  if (ns.startsWith('TaleWorlds.ScreenSystem') || ns.startsWith('TaleWorlds.ObjectSystem') || ns.startsWith('TaleWorlds.ServiceDiscovery')) return 'system';
  if (ns === 'Helpers') return 'system';
  return 'other';
}

const AREAS = {
  campaign: { zh: '战役系统 Campaign System', en: 'Campaign System', link: '/v1.3.15/zh/api/catalog-campaign' },
  mountandblade: { zh: '战斗与引擎绑定 Mount & Blade', en: 'Mount & Blade', link: '/v1.3.15/zh/api/catalog-mountandblade' },
  core: { zh: '核心 Core', en: 'Core', link: '/v1.3.15/zh/api/catalog-core' },
  engine: { zh: '引擎 Engine', en: 'Engine', link: '/v1.3.15/zh/api/catalog-engine' },
  library: { zh: '基础库 Library', en: 'Library', link: '/v1.3.15/zh/api/catalog-library' },
  ui: { zh: 'Gauntlet UI', en: 'Gauntlet UI', link: '/v1.3.15/zh/api/catalog-ui' },
  save: { zh: '存档系统 Save System', en: 'Save System', link: '/v1.3.15/zh/api/catalog-save' },
  system: { zh: '网络/平台/服务 Network & Platform', en: 'Network & Platform', link: '/v1.3.15/zh/api/catalog-system' },
  gameplay: { zh: '玩法模块 SandBox/StoryMode', en: 'Gameplay (SandBox/StoryMode)', link: '/v1.3.15/zh/api/catalog-gameplay' },
  localization: { zh: '本地化 Localization', en: 'Localization', link: '/v1.3.15/zh/api/catalog-localization' },
  input: { zh: '输入系统 Input', en: 'Input System', link: '/v1.3.15/zh/api/catalog-input' },
  other: { zh: '其它 Other', en: 'Other', link: '/v1.3.15/zh/api/catalog-other' },
};

const areaTypes = {};
for (const ns of Object.keys(byNs)) {
  const a = areaOf(ns);
  (areaTypes[a] = areaTypes[a] || {})[ns] = byNs[ns];
}

const KIND_ICON = { class: 'C', struct: 'S', interface: 'I', enum: 'E' };

function renderArea(areaKey, lang) {
  const a = AREAS[areaKey];
  const nss = areaTypes[areaKey] || {};
  const nsNames = Object.keys(nss).sort();
  let total = 0, docced = 0;
  for (const ns of nsNames) for (const ty of nss[ns]) { total++; if (isDocumented(ns, ty.name, lang)) docced++; }
  const L = lang === 'zh';
  const out = [];
  out.push('---');
  out.push('title: ' + (L ? a.zh : a.en) + ' 类目录 / Class Catalog');
  out.push('sidebar: auto');
  out.push('---');
  out.push('');
  out.push('# ' + (L ? a.zh : a.en) + (L ? ' 类目录' : ' Class Catalog'));
  out.push('');
  out.push(L
    ? `本页列出 ${VER} 源码中该领域全部公开类型，按命名空间分组。共 **${total}** 个类型，其中 **${docced}** 个已有深度文档页（带链接）。由 \`tools/gen-class-catalog.mjs\` 从源码自动生成。`
    : `This page lists every public type in this area from the ${VER} source, grouped by namespace. **${total}** types total, **${docced}** with a deep doc page (linked). Auto-generated by \`tools/gen-class-catalog.mjs\`.`);
  out.push('');
  out.push(L ? '> 图例：C=class S=struct I=interface E=enum。带 🔗 的已有深度文档页。' : '> Legend: C=class S=struct I=interface E=enum. 🔗 = has a deep doc page.');
  out.push('');
  for (const ns of nsNames) {
    const types = nss[ns].slice().sort((x, y) => x.name.localeCompare(y.name));
    out.push('## ' + ns + ' (' + types.length + ')');
    out.push('');
    out.push('| | 名称 Name | 文档 Doc |');
    out.push('|---|---|---|');
    for (const ty of types) {
      const dl = docLink(ns, ty.name, lang);
      const nameCell = dl ? `[${ty.name}](${dl})` : ty.name;
      out.push(`| ${KIND_ICON[ty.kind]} | ${nameCell} | ${dl ? '🔗' : '—'} |`);
    }
    out.push('');
  }
  return out.join('\n');
}

function renderIndex(lang) {
  const L = lang === 'zh';
  const out = [];
  out.push('---');
  out.push('title: ' + (L ? '完整类目录 / Complete Class Catalog' : 'Complete Class Catalog'));
  out.push('sidebar: auto');
  out.push('---');
  out.push('');
  out.push('# ' + (L ? '完整类目录' : 'Complete Class Catalog') + ' (v' + VER + ')');
  out.push('');
  let grandTotal = 0, grandDoc = 0;
  const rows = [];
  for (const key of Object.keys(AREAS)) {
    const nss = areaTypes[key] || {};
    let t = 0, d = 0;
    for (const ns of Object.keys(nss)) for (const ty of nss[ns]) { t++; if (isDocumented(ns, ty.name, lang)) d++; }
    grandTotal += t; grandDoc += d;
    const file = key === 'campaign' ? 'catalog-campaign' : 'catalog-' + key;
    rows.push(`| [${L ? AREAS[key].zh : AREAS[key].en}](./${file}) | ${t} | ${d} | ${t ? Math.round(d / t * 100) : 0}% |`);
  }
  out.push(L
    ? `${VER} 源码中全部 **${grandTotal}** 个 modder 相关公开类型（已剔除代码生成/第三方/网络协议），按领域分组。其中 **${grandDoc}** 个已有深度文档页。`
    : `All **${grandTotal}** modder-facing public types in the ${VER} source (codegen / 3rd-party / network protocol excluded), grouped by area. **${grandDoc}** have a deep doc page.`);
  out.push('');
  out.push('| 领域 Area | 类型数 Types | 已文档 Documented | 覆盖 Coverage |');
  out.push('|------|------|------|------|');
  out.push(...rows);
  out.push(`| **${L ? '合计 Total' : 'Total'}** | **${grandTotal}** | **${grandDoc}** | **${Math.round(grandDoc / grandTotal * 100)}%** |`);
  out.push('');
  out.push(L ? '::: tip 重新生成 / Regenerate\n源码更新后在仓库根目录运行：`node BannerlordCode.github.io/tools/gen-class-catalog.mjs`\n:::' : '::: tip Regenerate\nAfter source updates, run from repo root: `node BannerlordCode.github.io/tools/gen-class-catalog.mjs`\n:::');
  return out.join('\n');
}

mkdirSync(join(OUT, 'zh/api'), { recursive: true });
mkdirSync(join(OUT, 'en/api'), { recursive: true });
const fileMap = { campaign: 'catalog-campaign', mountandblade: 'catalog-mountandblade', core: 'catalog-core', engine: 'catalog-engine', library: 'catalog-library', ui: 'catalog-ui', save: 'catalog-save', system: 'catalog-system', gameplay: 'catalog-gameplay', localization: 'catalog-localization', input: 'catalog-input', other: 'catalog-other' };
for (const lang of ['zh', 'en']) {
  writeFileSync(join(OUT, lang, 'api/catalog.md'), renderIndex(lang));
  for (const key of Object.keys(AREAS)) {
    writeFileSync(join(OUT, lang, 'api/' + fileMap[key] + '.md'), renderArea(key, lang));
  }
}
console.log('Catalog generated. Documented types:', documented.size);
console.log('Areas:');
for (const key of Object.keys(AREAS)) {
  const nss = areaTypes[key] || {}; let t = 0, d = 0;
  for (const ns of Object.keys(nss)) for (const ty of nss[ns]) { t++; if (documented.has(ty.name)) d++; }
  console.log(`  ${key}: ${t} types, ${d} documented (${t ? Math.round(d / t * 100) : 0}%)`);
}
